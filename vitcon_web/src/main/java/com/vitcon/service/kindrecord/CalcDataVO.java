package com.vitcon.service.kindrecord;

public class CalcDataVO {
	
    private int avgVal;
	private int minVal;
	private int maxVal;
	private int calcVal;
    private int stdVal;
    
	public int getAvgVal() {
		return avgVal;
	}
	public void setAvgVal(int avgVal) {
		this.avgVal = avgVal;
	}
	public int getMinVal() {
		return minVal;
	}
	public void setMinVal(int minVal) {
		this.minVal = minVal;
	}
	public int getMaxVal() {
		return maxVal;
	}
	public void setMaxVal(int maxVal) {
		this.maxVal = maxVal;
	}
	public int getCalcVal() {
		return calcVal;
	}
	public void setCalcVal(int calcVal) {
		this.calcVal = calcVal;
	}
	public int getStdVal() {
		return stdVal;
	}
	public void setStdVal(int stdVal) {
		this.stdVal = stdVal;
	}

}
